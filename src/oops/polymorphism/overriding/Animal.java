package oops.polymorphism.overriding;

public class Animal {

	
	//Member variables
	
	//Constructor
	
	//Member Methods
	
	
	public void eat() {
		System.out.println("Inside Animal Class : Animal is eating" );
	}
	
	
	public void eat(String dogBreed) {
		System.out.println("Inside Animal Class : Animal is eating : " + dogBreed );
	}
	
	
	
}
