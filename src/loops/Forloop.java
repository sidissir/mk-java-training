package loops;

public class Forloop {

	public static void main(String[] args) {

		// loop variable => i
		// Loop Varibale intial value => 0
		// Loop Variable condition => i<=5
		// Increment or decrement oparators

		for (int i = 0; i <= 5; i++) {
			System.out.println(i);
		}

		
		System.out.println("=================================================");
		// loop variable => j
		// Loop Varibale intial value => 10
		// Loop Variable condition => j>=0
		// Increment or decrement oparators

		for (int j = 10; j >= 0; j--) {
			System.out.println(j);
		}
	}

}
