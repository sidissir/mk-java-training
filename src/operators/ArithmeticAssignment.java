package operators;

public class ArithmeticAssignment {

	public static void main(String[] args) {
		
		
		// +=
		
		
		int x = 23;
		int y = 7;
		
		y+=x;  
		//y = y+x;
		System.out.println(y);
		
		
		
		//-=
		
		int l = 30;
		int m = 10;
		
		l-=m;
		//l = l-m;
		System.out.println(l);
		
		
		
		//*=
		
		int a = 12;
		int b = 10;
		
		b *= a; 
		//b = b*a;
		System.out.println(b);
		
		
		// /=
		
		int p = 60;
		int q = 12;
		
		p /= q;
		//p = p/q;
		System.out.println(p);

	}

}
