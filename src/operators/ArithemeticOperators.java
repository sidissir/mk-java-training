package operators;

public class ArithemeticOperators {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;

		int sum;

		sum = a + b;

		System.out.println("After Addition : " + sum);

		int subtraction;
		subtraction = b - a;
		System.out.println("After Subtraction : " + subtraction);

		int mul;

		mul = a * b;

		System.out.println("After Multiplication :" + mul);

		int div;

		div = b / a;
		System.out.println("After Division :" + div);

		int mod;

		mod = b % a;
		System.out.println("After Mod :" + mod);
		
		
		
		

	}

}
